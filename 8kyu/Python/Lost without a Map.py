'''
Given an array of integers, return a new array with each value doubled.

For example:
[1, 2, 3] --> [2, 4, 6]
'''

# Method 1: For loop and new array
def maps(a):
    doubled = []
    for num in a:
        doubled.append(num * 2)
    return doubled

# Method 2: lambda function
def maps(a):
    return list(map((lambda n : 2*n), a))

# Method 3: shortening method 2 to one line by using two lambda functions
maps = lambda a : list(map((lambda n : 2*n), a))
