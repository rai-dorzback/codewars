/*
Create a min and max function which find the minimum
and maximum values in an array, respectively
*/

// METHOD 1
const min = arr => arr
    .sort((a, b) => a - b)[0];
const max = arr => arr
    .sort((a, b) => b - a)[0];

// METHOD 2
const min = a => Math.min(...a);
const max = a => Math.max(...a);